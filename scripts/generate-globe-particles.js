// ============ 点-多边形碰撞 (Ray Casting) ============

function pointInPolygon(lat, lon, polygon) {
  let inside = false;
  const n = polygon.length;
  for (let i = 0, j = n - 1; i < n; j = i++) {
    const [lati, loni] = polygon[i];
    const [latj, lonj] = polygon[j];
    if ((loni > lon) !== (lonj > lon) &&
        lat < (latj - lati) * (lon - loni) / (lonj - loni) + lati) {
      inside = !inside;
    }
  }
  return inside;
}

function isLand(lat, lon) {
  // 检查是否在任何大陆多边形内
  for (const continent of CONTINENTS) {
    if (pointInPolygon(lat, lon, continent.polygon)) {
      // 检查是否被海洋排除区覆盖
      for (const exc of OCEAN_EXCLUSIONS) {
        if (lat >= exc.latRange[0] && lat <= exc.latRange[1] &&
            lon >= exc.lonRange[0] && lon <= exc.lonRange[1]) {
          return false;
        }
      }
      return true;
    }
  }
  return false;
}

// ============ 球面采样 ============

/** 3D → 经纬度 */
function vec3ToLatLon(v) {
  const lat = Math.asin(v.y) * (180 / Math.PI);
  const lon = Math.atan2(v.z, v.x) * (180 / Math.PI);
  return { lat, lon: -lon };
}

/** Fibonacci 球面均匀采样 */
function fibonacciSphere(n) {
  const points = [];
  const phi = Math.PI * (3 - Math.sqrt(5));
  for (let i = 0; i < n; i++) {
    const y = 1 - (i / (n - 1)) * 2;
    const rAtY = Math.sqrt(1 - y * y);
    const theta = phi * i;
    points.push({
      x: Math.cos(theta) * rAtY,
      y: y,
      z: Math.sin(theta) * rAtY,
    });
  }
  return points;
}

function generate() {
  const spherePoints = fibonacciSphere(TOTAL);
  const ocean = [];
  const land = [];

  for (const pt of spherePoints) {
    const { lat, lon } = vec3ToLatLon(pt);
    const r = RADIUS;
    const pos = { x: pt.x * r, y: pt.y * r, z: pt.z * r };

    if (isLand(lat, lon)) {
      land.push(pos);
    } else {
      ocean.push(pos);
    }
  }

  console.log(`Total: ${TOTAL} | Ocean: ${ocean.length} | Land: ${land.length} (${((land.length / TOTAL) * 100).toFixed(1)}%)`);

  fs.writeFileSync(OUTPUT, JSON.stringify({ ocean, land }));
  console.log(`Written to ${OUTPUT}`);
}

generate();
